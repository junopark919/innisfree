const products = document.querySelector('.products__table');
const filter = document.querySelectorAll('.products__filter--option');
const category = filter[0];
const skinConcerns = filter[1];
const ingredients = filter[2];

ingredients.addEventListener('change', () => {
  const item = ingredients.value;
  const itemInfo = productsList[item];

  products.innerHTML = '';

  for (let i = 0; i < itemInfo.length; i++) {
    products.innerHTML += `<div class='products__table--item'>
        <img src=${itemInfo[i].img} alt=${
      itemInfo[i].name
    } class='products__img' />
        <p class='products__title'>${itemInfo[i].name}</p>
        <p class='products__price'>$${itemInfo[i].price}</p>
        <a class='products__cart'>${
          itemInfo[i].stock ? 'Add Cart' : 'Out of Stock'
        }</a>
      </div>`;
  }
});

